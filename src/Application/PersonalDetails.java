    private void rollNoListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rollNoListActionPerformed
        
        String selectedRollNo=rollNoList.getSelectedItem().toString();
        try
        {
            //Creating a new Database Connection
            MyDBConnection databaseConnection = new MyDBConnection();
            databaseConnection.init();
            Connection connection = databaseConnection.getMyConnection();
            Statement connectionStatement = connection.createStatement();
            
            
            //Getting Subject list of selected Class
            String query ="select name,email,guardian,dob from student where rollno ='"+selectedRollNo+"' and class ='"+className+"'";

            ResultSet resultSet;
            resultSet=connectionStatement.executeQuery(query);
            
            //Adding the subjects to drop down
            if(resultSet.next())
            {
                nameText.setText(resultSet.getString(1));
                emailText.setText(resultSet.getString(2));
                guardianText.setText(resultSet.getString(3));
                dobText.setText(resultSet.getString(4));
                
                
                studentNameList.setSelectedItem(resultSet.getString(1));
            }
            
            connection.close();
        }
        catch(Exception e)
        {
            System.out.println("Error");
        }
    }//GEN-LAST:event_rollNoListActionPerformed

