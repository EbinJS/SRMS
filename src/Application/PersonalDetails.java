    void initialise()
    {
        String studentName,rollNo;
        try
        {
            //Creating a new Database Connection   
            MyDBConnection databaseConnection = new MyDBConnection();
            databaseConnection.init();
            Connection connection = databaseConnection.getMyConnection();
            Statement connectionStatement = connection.createStatement();
            ResultSet resultSet;

            String query ="select name,rollno from main where class ='"+className+"' group by name order by rollno";
            resultSet=connectionStatement.executeQuery(query);
            

            while(resultSet.next())
            {
                studentName=resultSet.getString(1);
                rollNo=resultSet.getString(2);
                studentNameList.addItem(studentName);
                rollNoList.addItem(rollNo);
            }
            
            connection.close();
        }
        catch(Exception e)
        {
            System.out.println(e); 
        }
    }

